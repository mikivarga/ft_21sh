#include "ft_21sh.h"

void				ft_echo(char **arr)
{
	int				fd;

	fd = 1;
	while (*arr)
	{
		write(fd, *arr, ft_strlen(*arr));
		if (*(++arr))
			write(fd, " ", 1);
	}
	write(fd, "\n", 1);
}
